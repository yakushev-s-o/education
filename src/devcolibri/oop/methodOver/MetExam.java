package devcolibri.oop.methodOver;

public class MetExam {

    public String get() {
        return "Hello";
    }

    public String get(String s) {
        return s;
    }

    public String get(String s1, String s2) {
        return s1 + " " + s2;
    }

    public String get(int n) {
        return "Number: " + n;
    }

}
