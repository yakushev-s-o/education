package devcolibri;

public class Variables {

    static byte b = 1;
    static short s = 5;
    static int i = 10;
    static float f = 11.1f;
    static double d = 22.2;
    static boolean bt = true;
    static boolean bf = false;

    public static void main(String[] args) {

        System.out.println("byte: " + b + "\n" +
                "short: " + s + "\n" +
                "int: " + i + "\n" +
                "float: " + f + "\n" +
                "double: " + d + "\n" +
                "boolean true: " + bt + "\n" +
                "boolean false: " + bf);
    }
}
